'''
Write the necessary code to display the area and perimeter of a rectangle
that has a width of 2.4 and a height of 6.4

'''
"""
w = 2.4
h = 6.4
"""

def perimeter (w, h):
    width = 2*w
    height = 2*h
    result = (width + height)
    print(result)
perimeter(2.4, 6.4)

def area (w, h):
    width = w
    height = h
    result = (width*height)
    print(result)
area(2.4, 6.4)
